export function getCityById(id: string): City | undefined {
  return CITIES.find((city) => city.id === id);
}

export function searchCities(query: string): City[] {
  const lowerQuery = query.toLowerCase();
  return CITIES.filter(
    (city) =>
      city.name.toLowerCase().includes(lowerQuery) ||
      city.nameEn.toLowerCase().includes(lowerQuery) ||
      city.country.toLowerCase().includes(lowerQuery),
  );
}

export function getCitiesByCountryCode(countryCode: string): City[] {
  return CITIES.filter(
    (city) => city.countryCode.toLowerCase() === countryCode.toLowerCase(),
  );
}

export function getCitiesByTimezone(timezone: string): City[] {
  return CITIES.filter((city) => city.timezone === timezone);
}

export function getUniqueCountries(): {
  name: string;
  code: string;
  flag: string;
}[] {
  const countryMap = new Map<
    string,
    { name: string; code: string; flag: string }
  >();
  for (const city of CITIES) {
    if (!countryMap.has(city.countryCode)) {
      countryMap.set(city.countryCode, {
        name: city.country,
        code: city.countryCode,
        flag: city.flag,
      });
    }
  }
  return Array.from(countryMap.values()).sort((a, b) =>
    a.name.localeCompare(b.name, "ja"),
  );
}
